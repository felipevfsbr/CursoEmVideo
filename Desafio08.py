n = float(input('Digite um Valor em Metro:'))

print('O Valor em milimetro é {}mm'.format(n*1000))
print('O Valor em centimetro é {}cm'.format(n*100))
print('O Valor em decimetro é {}dm'.format(n*10))
print('O Valor em metro é {}m'.format(n))
print('O Valor em decametro é {}dc'.format(n/10))
print('O Valor em hectometro é {}hm'.format(n/100))
print('O Valor em quilometro é {}km'.format(n/1000))
